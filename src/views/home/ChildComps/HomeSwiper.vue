<template>
  <swiper  ref="swiper" v-if="banners.length">
      <swiper-item v-for="(item, index) in banners"
                   :key="index">
        <a :href="item.link">
          <img :src="item.image" alt="" @load="imageLoad">
        </a>
      </swiper-item>
    </swiper>
</template>

<script>
  import {Swiper, SwiperItem} from 'components/common/swiper'

  export default {
    name: "HomeSwiper",
    components: {
      Swiper,
      SwiperItem
    },
    props: {
      banners: {
        type: Array,
        default() {
          return []
        }
      }
    },
    data() {
      return {
        isLoad: false
      }
    },
    methods: {
      imageLoad() {
        // v-once代替
        if(!this.isLoad) {
          this.$emit('swiperImageLoad')
          this.isLoad = true
        }
        // this.$emit('swiperImageLoad')
      }
    }
  }
</script>

